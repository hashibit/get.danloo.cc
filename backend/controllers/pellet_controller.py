router = APIRouter()


def convert_pellet_to_response(pellet: PelletDB) -> PelletData:
    """Convert PelletDB to PelletResponse format"""
    view_count = 0
    if pellet.counters:
        view_count = pellet.counters.view_count

    tags = [TagData.model_validate(tag) for tag in pellet.tags]
    is_gold = any(tag.name == "gold" for tag in tags)

    pellet_data = PelletData.model_validate(pellet)
    pellet_data.tags = tags
    pellet_data.view_count = view_count
    pellet_data.is_gold = is_gold

    return pellet_data


@router.get("/", response_model=PelletListResponse)
async def get_pellets(
    limit: int | None = 10,
    sortBy: str | None = "createdAt",
    sortOrder: str | None = "desc",
    db: Session = Depends(get_database),
    current_user: dict = Depends(get_current_user),
):
    """Get pellets list for current user"""
    try:
        pellets = pellet_service.get_pellets_by_user(
            db,
            current_user["user_id"],
            limit=limit,
            sort_by=sortBy,
            sort_order=sortOrder,
        )
        converted_pellets = [convert_pellet_to_response(pellet) for pellet in pellets]
        return PelletListResponse(
            pellets=converted_pellets,
            pagination={"limit": limit, "offset": 0, "total": len(converted_pellets)},
        )
    except ValueError as e:
        logger.error("failed get_pellets_by_user, ", exc_info=e)
        raise HTTPException(status_code=400, detail=str(e))


